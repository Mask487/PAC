public class App{
    
    String title;
    String dev;
    
    App(String title, String dev){
        this.title = title;
        this.dev = dev;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDev() {
        return dev;
    }

    public void setDev(String dev) {
        this.dev = dev;
    }
    
    
}